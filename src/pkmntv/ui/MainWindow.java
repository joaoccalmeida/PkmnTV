    private void showPkmnInfo(String name){
        // For the first query is not necessary to clear the type panels.
        if(isFirstQuery)
            isFirstQuery = false;
        else
            clearTypeChart();
        
        Pokemon pkmn = ig.getPkmnInfo(name);
        if(pkmn == null)
            JOptionPane.showMessageDialog(this, 
                    "The Pokemon doesn¬¥t exist.\nPlease try again.", 
                    "Pokemon not found", JOptionPane.ERROR_MESSAGE);
        else{
            String fstType = pkmn.getFirstType();
            String sndType = pkmn.getSecondType();

            if(!ig.getPkmnTypeEffect(fstType, sndType))
                JOptionPane.showMessageDialog(this, 
                        "Could not get the Pokemon type effectivness.\n"
                                + "Please try again.", 
                        "Error", JOptionPane.ERROR_MESSAGE);
            else{
                if(showImgMenuItem.isSelected()){
                    displayPkmnImage(name);
                }
                idNameLabel.setText(pkmn.getName());
                alterTypeButtons(pkmn.getFirstType(), pkmn.getSecondType());
            
                insertTypeChartButtons(normalPanel, TypeEffect.getNormalEffectMap());
                insertTypeChartButtons(weakPanel, TypeEffect.getWeakEffectMap());
                insertTypeChartButtons(immunePanel, TypeEffect.getImmuneEffectMap());
                insertTypeChartButtons(resistantPanel, TypeEffect.getResistantEffectMap());
                
                typeChartPanel.repaint();
                this.pack();
            }
        }  
    }
    
    private void displayPkmnImage(String name){
        URL url = getClass().getClassLoader().getResource("resources/sprites/" + name + ".gif");
        if(url == null)
            JOptionPane.showMessageDialog(this, 
                        "Could not get the Pokemon sprite.\n"
                                + "Please try again.", 
                        "Error", JOptionPane.ERROR_MESSAGE);
        else
            imgLabel.setIcon(new ImageIcon(url));
    }
    
    private void alterTypeButtons(String type1, String type2){
        type1Button.setText(type1);
        type1Button.setBackground(TypeColorMatcher.TC_MAP.get(type1));
        type2Button.setText(type2);
        type2Button.setBackground(TypeColorMatcher.TC_MAP.get(type2));
    }
    
    private void clearTypeChart(){
        normalPanel.removeAll();
        weakPanel.removeAll();
        immunePanel.removeAll();
        resistantPanel.removeAll();
        
        TypeEffect.resetTypeMap();
    }
    
    private void insertTypeChartButtons(JPanel panel, Map map){
        map.forEach((k,v)-> {
            JButton jb = new JButton(k+": "+v+"x");
            jb.setVisible(true);
            jb.setBackground(TypeColorMatcher.TC_MAP.get((String)k));
            panel.add(jb);
                });
    }
    
    
    
